// ---- deklaracje z lcd.c ----
extern void lcd_set_window(int x, int y, int width, int height);
extern void lcd_cmd(uint8_t cmd);
extern void lcd_data16(uint16_t value);
extern void lcd_data(uint8_t data);

#define ST7735S_RAMWR 0x2C

// ---------------- CHAR ----------------
void lcd_draw_char(int x, int y, char c, uint16_t color, uint16_t bg)
{
    if (c >= 'a' && c <= 'z') c -= 32;
    if (c < 'A' || c > 'Z') return;

    int idx = c - 'A';

    lcd_set_window(x, y, 12, 16);
    lcd_cmd(ST7735S_RAMWR);

    for (int row = 0; row < 16; row++)
    {
        uint16_t line = font12x16[idx][row];

        for (int col = 0; col < 12; col++)
        {
            if (line & (1 << (11 - col)))
                lcd_data16(color);
            else
                lcd_data16(bg);
        }
    }
}

// ---------------- STRING ----------------
void lcd_print(int x, int y, const char *str, uint16_t color, uint16_t bg)
{
    while (*str)
    {
        lcd_draw_char(x, y, *str, color, bg);
        x += 12;
        str++;
    }
}



void lcd_print_line(int line, const char *str, uint16_t color, uint16_t bg)
{
    int x = 10;
    int y = (line - 1) * LINE_H;

    lcd_clear_line(y, bg);   // 👈 KLUCZOWE

    lcd_print(x, y, str, color, bg);
}

void lcd_clear_line(int y, uint16_t bg)
{
    lcd_set_window(0, y, 160, LINE_H); // albo szerokość Twojego LCD
    lcd_cmd(ST7735S_RAMWR);

    for (int i = 0; i < 160 * LINE_H; i++)
    {
        lcd_data16(bg);
    }
}


void lcd_clear(void)
{
	lcd_clear_line(0 * LINE_H, BLACK);
	lcd_clear_line(1 * LINE_H, BLACK);
	lcd_clear_line(2 * LINE_H, BLACK);
	lcd_clear_line(3 * LINE_H, BLACK);
	lcd_clear_line(4 * LINE_H, BLACK);
	lcd_clear_line(5 * LINE_H, BLACK);
	lcd_clear_line(6 * LINE_H, BLACK);

}



